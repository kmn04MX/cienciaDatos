""" La funcion type sirve para ver el tipo de dato que contiene una variable, por ejemplo, el siguiente
codigo """

var1 = 5;
var2 = 4.5;
var3 = "Hola"
var4 = True;

print(f"Para enteros --> {type(var1)} \nPara flotantes --> {type(var2)}\nPara strings --> {type(var3)}\nPara Booleanos --> {type(var4)}")

